SPECIAL_PATTERNS = [
    (re.compile(r'新大阪駅'), ('関西', '大阪府', '大阪市')),
    (re.compile(r'^大阪福島区'), ('関西', '大阪府', '大阪市')),
    (re.compile(r'京丹後市'), ('関西', '京都府', '京丹後市')),
]

def parse_location(address):
    if not address or not address.strip():
        return '不明', '不明', '不明'
    address = address.strip()

    for pattern, result in SPECIAL_PATTERNS:
        if pattern.search(address):
            return result

    # まず都道府県名で始まるかチェック
    m = PREF_PATTERN.match(address)
    if m:
        pref = m.group(1)
        rest = m.group(2)
        region = PREF_TO_REGION.get(pref, '不明')
        city_m = re.match(r'([^\d０-９]+?[市区町村])', rest)
        city = city_m.group(1) if city_m else ''
        return region, pref, city

    # 都道府県省略 → 市区町村名で逆引き
    city_m = re.match(r'([^\d０-９]+?[市区町村])', address)
    if city_m:
        city = city_m.group(1)
        pref = CITY_TO_PREF.get(city)
        if pref:
            region = PREF_TO_REGION.get(pref, '不明')
            return region, pref, city

    return '不明', '不明', '不明'

def main():
    with open(DATA_PATH) as f:
        data = json.load(f)

    fixed = 0
    for r in data:
        if r.get('region') == '不明' or not r.get('region'):
            region, pref, city = parse_location(r.get('address', ''))
            if region != '不明':
                r['region'] = region
                r['prefecture'] = pref
                r['city'] = city
                fixed += 1

    with open(DATA_PATH, 'w') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f'修正: {fixed}件')
    still_unknown = sum(1 for r in data if r.get('region') == '不明')
    print(f'なお不明: {still_unknown}件')

if __name__ == '__main__':
    main()
